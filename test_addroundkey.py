from aes.add_round_key import AddRoundKey

state = [

    [0x04,0xE0,0x48,0x28],
    [0x66,0xCB,0xF8,0x06],
    [0x81,0x19,0xD3,0x26],
    [0xE5,0x9A,0x7A,0x4C]

]

round_key = [

0xA0,0x88,0x23,0x2A,
0xFA,0x54,0xA3,0x6C,
0xFE,0x2C,0x39,0x76,
0x17,0xB1,0x39,0x05

]

before, key_matrix, after = AddRoundKey.apply(state, round_key)

print("===== BEFORE =====")

for row in before:
    print(" ".join(f"{x:02X}" for x in row))

print()

print("===== ROUND KEY =====")

for row in key_matrix:
    print(" ".join(f"{x:02X}" for x in row))

print()

print("===== AFTER =====")

for row in after:
    print(" ".join(f"{x:02X}" for x in row))
